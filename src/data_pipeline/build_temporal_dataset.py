"""Build turn-sequence dataset for the Temporal CFI Transformer (Phase 2, pillar 2).

Takes the SMILE-style synthetic dialogues (data/synthetic/dialogues.jsonl) and runs
the existing DistortionClassifier over each CLIENT turn, in order, producing one
training sequence per dialogue:

    {"dialogue_index": int,
     "turns": [{"turn": 1, "distortions": {label: prob}, "cfi": float}, ...]}

The temporal model consumes the 5-dim distortion vectors; CFI is included for
reporting only (single source of truth: compute_cfi()).

LIMITATION (disclose in paper, same pattern as weak labeling): these are synthetic
dialogue trajectories generated by an LLM, not real longitudinal user data. The
temporal model trained on them validates the architecture, not clinical dynamics.

Usage: python -m src.data_pipeline.build_temporal_dataset [--limit N]
"""

from __future__ import annotations

import argparse
import json

from src.metrics.cognitive_flexibility_index import compute_cfi
from src.utils.config import PROJECT_ROOT, load_config
from src.utils.logging_config import get_logger

logger = get_logger(__name__)


def run(limit: int | None = None) -> None:
    from src.models.distortion_classifier import DistortionClassifier

    cfg = load_config("data")
    in_path = PROJECT_ROOT / cfg["paths"]["synthetic_dir"] / "dialogues.jsonl"
    out_path = PROJECT_ROOT / cfg["paths"]["processed_dir"] / "temporal_sequences.jsonl"
    out_path.parent.mkdir(parents=True, exist_ok=True)

    dialogues = [json.loads(line) for line in in_path.read_text().splitlines() if line.strip()]
    if limit:
        dialogues = dialogues[:limit]
    logger.info("Building temporal sequences from %d dialogues", len(dialogues))

    classifier = DistortionClassifier.load()
    written = 0
    with open(out_path, "w") as out:
        for d in dialogues:
            client_turns = [t["text"] for t in d["turns"] if t["role"] == "client"]
            if len(client_turns) < 2:  # need at least one transition to learn from
                continue
            sequence = []
            for i, text in enumerate(client_turns, start=1):
                probs = classifier.predict(text)
                sequence.append({"turn": i, "distortions": probs, "cfi": compute_cfi(probs)})
            out.write(
                json.dumps({"dialogue_index": d.get("source_index", written), "turns": sequence})
                + "\n"
            )
            written += 1
            if written % 10 == 0:
                logger.info("  %d/%d sequences", written, len(dialogues))

    logger.info("Done: %d sequences -> %s", written, out_path)


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--limit", type=int, default=None)
    args = parser.parse_args()
    run(args.limit)
